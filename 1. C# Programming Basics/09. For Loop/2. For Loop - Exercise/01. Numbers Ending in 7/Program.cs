using System;
using System.Collections.Generic;
using System.Linq;

namespace _01.Numbers_Ending_in_7
{
    public class Program
    {
        public static void Main(string[] args)
        {
            for (int i = 7; i < 1000; i += 10)
            {
                Console.WriteLine(i);
            }
        }
    }
}
